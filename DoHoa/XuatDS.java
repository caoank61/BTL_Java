    public void initTable(){
        tblModel = new DefaultTableModel();
        tblModel.setColumnIdentifiers(new String[] {"MSHS","Họ Tên", "Lớp", "Địa Chỉ","Ngày", "Tháng", "Năm", "Điểm Toán","Điểm Lý","Điểm Hóa","Điểm Văn","Điểm Sư","Điểm Địa", "Điểm TB"});
        jTable1.setModel(tblModel);
    }
    public void loadData(){
        Hoc_Sinhs hoc_sinhs = new Hoc_Sinhs();
        hoc_sinhs.connect();
        tblModel.setRowCount(0);
        for(int i=0; i< hoc_sinhs.size() ;i++){
            Object[] row = new Object[] {
            hoc_sinhs.elementAt(i).getMSHS(),
            hoc_sinhs.elementAt(i).getHoTen(),
            hoc_sinhs.elementAt(i).getLop(),
            hoc_sinhs.elementAt(i).getDia_Chi(),
            hoc_sinhs.elementAt(i).getNgay(),
            hoc_sinhs.elementAt(i).getThang(),
            hoc_sinhs.elementAt(i).getNam(),
            hoc_sinhs.elementAt(i).getDiem_Toan(),
            hoc_sinhs.elementAt(i).getDiem_Ly(),
            hoc_sinhs.elementAt(i).getDiem_Hoa(),
            hoc_sinhs.elementAt(i).getDiem_Van(),
            hoc_sinhs.elementAt(i).getDiem_Su(),
            hoc_sinhs.elementAt(i).getDiem_Dia(),
            hoc_sinhs.elementAt(i).Tinh_DiemTB(hoc_sinhs.elementAt(i).getDiem_Toan(),hoc_sinhs.elementAt(i).getDiem_Ly(),hoc_sinhs.elementAt(i).getDiem_Hoa(),hoc_sinhs.elementAt(i).getDiem_Van(),hoc_sinhs.elementAt(i).getDiem_Su(),hoc_sinhs.elementAt(i).getDiem_Dia()),
            };  
            tblModel.addRow(row);
    }
        tblModel.fireTableDataChanged();
};  

    
